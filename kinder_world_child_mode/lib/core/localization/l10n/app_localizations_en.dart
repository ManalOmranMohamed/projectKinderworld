  @override
  String subscriptionStatusLabel(String status) {
    switch (status.toLowerCase()) {
      case 'active':
        return 'Active';
      case 'canceled':
        return 'Canceled';
      case 'failed':
        return 'Failed';
      case 'past_due':
        return 'Past Due';
      case 'pending_activation':
        return 'Pending Activation';
      case 'pending':
        return 'Pending';
      case 'action_required':
        return 'Action Required';
      case 'refunded':
        return 'Refunded';
      case 'succeeded':
        return 'Succeeded';
      case 'paid':
        return 'Paid';
      case 'failure':
        return 'Failure';
      case 'activation_failed':
        return 'Activation Failed';
      case 'checkout_failed':
        return 'Checkout Failed';
      case 'refund_failed':
        return 'Refund Failed';
      case 'not_applicable':
        return 'Not Applicable';
      case 'refund':
        return 'Refund';
      case 'charge':
        return 'Charge';
      case 'checkout':
        return 'Checkout';
      case 'portal':
        return 'Portal';
      case 'billing_portal':
        return 'Billing Portal';
      default:
        return status
            .replaceAll('_', ' ')
            .split(' ')
            .where((part) => part.isNotEmpty)
            .map((part) => '${part[0].toUpperCase()}${part.substring(1)}')
            .join(' ');
    }
  }

